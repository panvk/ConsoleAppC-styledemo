using System;

namespace ConsoleApp1
{
    class Program
    {
        static void Main(string[] args)
        {
            Console.WriteLine("Hello World!");
            Console.WriteLine("第二行");
            Console.Write("不换行,");
            Console.Write("然后");
            Console.WriteLine(12.3m); //用"M"或者"m"表示十进制
        }
    }
}




